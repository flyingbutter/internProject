    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (jTextField3.getText().length() > 0) {
            int ticketCount = Integer.parseInt(jTextField3.getText());

            for (int i = 0; i < ticketCount; i++) {
                newPromo.addDiscount(Ticket.valueOf(jComboBox2.getSelectedItem().toString()));
                listModelds.addElement(Ticket.valueOf(jComboBox2.getSelectedItem().toString()));
            }

            jList2.setModel(listModelds);
        }
        if (jTextField4.getText().length() > 0) {
            newPromo.setCashDiscount(Double.parseDouble(jTextField4.getText()));
        } else {
            newPromo.setCashDiscount(0);
        }


    }//GEN-LAST:event_jButton2ActionPerformed

