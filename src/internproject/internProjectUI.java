    private void btnGetPriceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGetPriceActionPerformed
        // TODO add your handling code here:
        double rawPrice = myCart.total();
        double discount = 0;

        /**
         * FIX THIS
         */
        for (Promotion element : promotionList) {
            System.out.println(element.getRequired());
            boolean eligible = true;
            for (Ticket e : Ticket.values()) {    
                System.out.println(Collections.frequency(element.getRequired(), e));
                if (Collections.frequency(element.getRequired(), e) > Collections.frequency(myCart.getList(), e)) {
                    eligible = false;
                }

               
            }
             if (eligible == true) {
                    discount += element.totalDiscount();
                    discount += element.getCashDiscount();
                    System.out.println(discount);
                }

        }

        /**
         * TO HERE
         */
        jTextField1.setText(Double.toString(rawPrice - discount));

    }//GEN-LAST:event_btnGetPriceActionPerformed

    private void btnAddToCartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddToCartActionPerformed

        int ticketCount = Integer.parseInt(ticketValue.getText());
        if (ticketCount > 0) {
            for (int i = 0; i < ticketCount; i++) {
                myCart.add(Ticket.valueOf(jComboBox3.getSelectedItem().toString()));
                listModel.addElement(Ticket.valueOf(jComboBox3.getSelectedItem().toString()));
            }

            jList1.setModel(listModel);

        }

    }//GEN-LAST:event_btnAddToCartActionPerformed

